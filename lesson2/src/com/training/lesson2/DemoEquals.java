package com.training.lesson2;

/**
 * ==，和=的区别
 */
public class DemoEquals {
    public static void main(String[] args) {
        System.out.println(1==1);//true
        System.out.println(1<2);//true
        System.out.println(3>4);//false
        System.out.println(3<=4);//true
        System.out.println(3>=4);//false
        System.out.println(3!=4);//true
    }
}
